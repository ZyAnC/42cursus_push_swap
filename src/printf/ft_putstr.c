#include "printf.h"

int	ft_putstr(char *str)
{
	int	i;

	i = 0;
	if (!str)
	{
		if (write (1, "(null)", 6) != 6)
			return (-1);
		return (6);
	}
	while (str[i] != '\0')
		i++;
	return (write(1, str, i));
}
